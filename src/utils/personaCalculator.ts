/**
 * 일간 오행 + 십성 그룹 + 오행 분포를 받아 학습 페르소나 결과를 반환합니다.
 * 과다(2개 이상) / 결핍(0개) 오행에 대한 처방도 함께 반환합니다.
 */
export function calcPersona(
  element: Element,
  sipseongGroup: TenGodGroup,
  fiveElements: Record<Element, number>,
): PersonaResult {
  const key = `${element}_${sipseongGroup}`
  const persona = PERSONA_DB[key] ?? {
    emoji: '⭐',
    title: '균형잡힌 밸런스형',
    keywords: ['#꾸준함', '#자기페이스'],
    guide: '자신만의 페이스를 유지하며 꾸준히 공부하세요.',
  }

  // 과다(≥2) · 결핍(0) 처방 — 최대 2개로 제한
  const troubles: string[] = []
  for (const [el, count] of Object.entries(fiveElements) as [Element, number][]) {
    if (troubles.length >= 2) break
    if (count >= 2) troubles.push(TROUBLE_DB[el].excess)
    else if (count === 0) troubles.push(TROUBLE_DB[el].deficiency)
  }

  return { persona, troubles }
}
